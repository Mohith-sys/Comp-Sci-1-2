/*
 Multiline Comment
 */
public class PrintStatement {
	// main method begins
	public static void main(String[] args) {
		// Single line comment
		System.out.print("Hello, Good Morning!");
		System.out.println("It is a snowy day!");
		System.out.println("My name is Mohith");
		
		//prints 2\9\2021
		
		System.out.println("2\\9\\2021");
		// \n - new line
		// \t - tab
		// \\ - one \
		
		System.out.print("Good \n Morning! \t"
				+ "Will it snow tomorrow?");
		// ASCII ART
		// 5 lines of design using symbols
		
		System.out.print("\n            __,__\r\n"
				+ "   .--.  .-\"     \"-.  .--.\r\n"
				+ "  / .. \\/  .-. .-.  \\/ .. \\\r\n"
				+ " | |  '|  /   Y   \\  |'  | |\r\n"
				+ " | \\   \\  \\ 0 | 0 /  /   / |\r\n"
				+ "  \\ '- ,\\.-\"`` ``\"-./, -' /\r\n"
				+ "   `'-' /_   ^ ^   _\\ '-'`\r\n"
				+ "       |  \\._   _./  |\r\n"
				+ "       \\   \\ `~` /   /\r\n"
				+ "        '._ '-=-' _.'\r\n"
				+ "           '~---~'");
		
		
	}

}
