package Unit2;

public class DebugTwo2 {

	public static void main(String[] args) {
		int a, b, sum, diff, prod;
	    a = 7;
	    b = 4;
	    sum = a + b;
	    diff = a - b;
	    prod = a * b;
	    System.out.println("The sum is " + sum);
	    System.out.println("The difference is " + diff);
	    System.out.println("The product is " + prod);

	}

}
