public class CharDemo {

	public static void main(String[] args) {
		
		char character;
		character = 'A';
		System.out.println(character);
		
		character = 85;
		System.out.println(character);
		
		char ch =  'd';
		System.out.println(ch);
		
		ch = 65; // <127
		System.out.println(ch);
		
		ch++; // ch = ch + 1
		System.out.println(ch);

	}

}
