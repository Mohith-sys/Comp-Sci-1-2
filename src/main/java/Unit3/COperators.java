public class COperators {

	public static void main(String[] args) {
		// Relations operators are only for
		// primitive data types
		// int, double, char, boolean
		
		// <  >  <=  >=  ==  !=
		
		int x = 5, y = -10;
		
		if (x < y)
			System.out.println("x < y");
		
		else if (x > y)
			System.out.println("x < y");
		
		else
			System.out.println("x = y");

	}

}
